package vo;

public class FaqVO {

	private String faqCode;
	private String admCode;
	private String faqTitle;
	private String faqCon;
	private String faqDate;
	
	public String getFaqCode() {
		return faqCode;
	}
	public void setFaqCode(String faqCode) {
		this.faqCode = faqCode;
	}
	public String getAdmCode() {
		return admCode;
	}
	public void setAdmCode(String admCode) {
		this.admCode = admCode;
	}
	public String getFaqTitle() {
		return faqTitle;
	}
	public void setFaqTitle(String faqTitle) {
		this.faqTitle = faqTitle;
	}
	public String getFaqCon() {
		return faqCon;
	}
	public void setFaqCon(String faqCon) {
		this.faqCon = faqCon;
	}
	public String getFaqDate() {
		return faqDate;
	}
	public void setFaqDate(String faqDate) {
		this.faqDate = faqDate;
	}
	
	
	
	
}
